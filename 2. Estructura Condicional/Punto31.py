# 31. Leer dos números enteros y determinar si la diferencia entre los dos es un número par
print("Hola usuario, ingrese 2 números enteros y determinaré si la diferencia entre los dos es un número par")

Numero1=int(input("Ingrese el primer número por favor: "))
Numero2=int(input("Ingrese el primer número por favor: "))

resultado=0
Numero1-Numero2==resultado
